class Primitive
{
private:
    std::vector<Vertex> vertices;
    std::vector<GLint> indices;

public:
    Primitive(){};
    
    void Set(const float* data, const int& data_count);
    void Set(const float* data, const int& data_count, const GLint* indices, const int& indices_count);
    
    inline Vertex GetVertex(const size_t k) const { return this->vertices[k]; };
    inline GLint GetIndex(const size_t k) const { return this->indices[k]; };
    inline size_t GetVerticesCount() const { return vertices.size(); };
    inline size_t GetIndicesCount() const { return indices.size(); };
};

